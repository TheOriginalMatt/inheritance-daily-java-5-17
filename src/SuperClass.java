public class SuperClass {
	private int superInt;

	/**
	* Gets the int stored in the super class
	*
	*@param val int to be stored in the super class
	*
	*/
	public void setInt(int val) {
		this.superInt = val;
	}

	/**
	* Gets the int stored in the super class
	*
	*@return int stored in the super class
	*
	*/
	public int getInt() {
		return this.superInt;
	}
}